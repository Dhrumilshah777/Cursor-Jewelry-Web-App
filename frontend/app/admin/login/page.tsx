'use client';

import { useState, useEffect } from 'react';
import { useRouter, useSearchParams } from 'next/navigation';
import { setAdminKey, clearAdminKey, apiGet, getApiBase } from '@/lib/api';

export default function AdminLoginPage() {
  const [key, setKey] = useState('');
  const [error, setError] = useState('');
  const [loading, setLoading] = useState(false);
  const router = useRouter();
  const searchParams = useSearchParams();

  useEffect(() => {
    const err = searchParams.get('error');
    if (err === 'not_allowed') setError('Your Google account is not allowed to access admin.');
    else if (err === 'google_denied') setError('Google sign-in was cancelled or failed.');
    else if (err === 'google_not_configured') setError('Google sign-in is not configured. Use admin key.');
  }, [searchParams]);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');
    if (!key.trim()) {
      setError('Enter the admin key.');
      return;
    }
    setLoading(true);
    try {
      setAdminKey(key.trim());
      await apiGet('/api/admin/products', true);
      router.push('/admin');
      router.refresh();
    } catch {
      setError('Invalid admin key.');
      clearAdminKey();
    } finally {
      setLoading(false);
    }
  };

  const googleLoginUrl = `${getApiBase()}/api/auth/google`;

  return (
    <div className="flex min-h-screen items-center justify-center bg-stone-100 p-4">
      <div className="w-full max-w-sm rounded-lg border border-stone-200 bg-white p-6 shadow-sm">
        <h1 className="text-xl font-semibold text-charcoal">BLURE Admin</h1>
        <p className="mt-1 text-sm text-stone-500">Sign in with Google or use your admin key.</p>

        <a
          href={googleLoginUrl}
          className="mt-6 flex w-full items-center justify-center gap-2 rounded border border-stone-300 bg-white px-3 py-2.5 text-sm font-medium text-charcoal shadow-sm transition-colors hover:bg-stone-50"
        >
          <svg className="h-5 w-5" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
          </svg>
          Sign in with Google
        </a>

        <div className="mt-4 flex items-center gap-3 text-stone-400">
          <span className="flex-1 border-t border-stone-200" />
          <span className="text-xs">or</span>
          <span className="flex-1 border-t border-stone-200" />
        </div>

        <form onSubmit={handleSubmit} className="mt-4">
          <input
            type="password"
            value={key}
            onChange={(e) => setKey(e.target.value)}
            placeholder="Admin key"
            className="w-full rounded border border-stone-300 px-3 py-2 text-charcoal placeholder-stone-400 focus:border-stone-500 focus:outline-none focus:ring-1 focus:ring-stone-500"
            autoComplete="off"
          />
          {error && <p className="mt-2 text-sm text-red-600">{error}</p>}
          <button
            type="submit"
            disabled={loading}
            className="mt-4 w-full rounded bg-charcoal px-3 py-2 text-sm font-medium text-white hover:bg-stone-800 disabled:opacity-50"
          >
            {loading ? 'Checkingâ€¦' : 'Sign in with key'}
          </button>
        </form>
      </div>
    </div>
  );
}
